import { AUTHCREATE } from "../constants/actionTypes";

export default (dataAkun = [], action) => {
    switch (action.type) {
        case 'AUTHCREATE':
            return [...dataAkun, action.payload]; // send an array first spread all of the patients then add new patient and then the new patient will stored in the action.payload
        default:
            return dataAkun;
    }
}